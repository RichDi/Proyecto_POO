    private boolean connect_and_catch(String sql, String mensaje, String fail, String user, String password){
        boolean success = false;
        try{            
            Class.forName("com.mysql.jdbc.Driver");
            String cadena = "jdbc:mysql://localhost/fpoo?user="+user+"&password="+password;
            Connection con = (Connection) DriverManager.getConnection(cadena);            
            PreparedStatement stmt = null;            
            stmt=con.prepareStatement(sql);
            int sw=stmt.executeUpdate();            
            if(sw!=0){
                JOptionPane.showMessageDialog(null,mensaje);                               
                success = true;
            }                                    
        }catch(ClassNotFoundException e1){
            JOptionPane.showMessageDialog(null,e1);
            JOptionPane.showMessageDialog(null,fail);            
        }catch(SQLException e2){
            JOptionPane.showMessageDialog(null,e2);
            JOptionPane.showMessageDialog(null,fail);            
        }catch(Exception e3){
            JOptionPane.showMessageDialog(null,e3);
            JOptionPane.showMessageDialog(null,fail);
        }
        return success;
    }

